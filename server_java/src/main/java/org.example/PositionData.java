package com.example;

public class PositionData {
    private double x;
    private double y;
    private double rx;
    private double ry;

    public PositionData() {}

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getRx() {
        return rx;
    }

    public double getRy() {
        return ry;
    }
}
